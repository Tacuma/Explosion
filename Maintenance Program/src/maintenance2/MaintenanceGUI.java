    public MaintenanceGUI() throws FileNotFoundException {
        
        initComponents();
        File file = new File("inp.txt");
        Scanner input = new Scanner(file);
        String set;// = input.nextLine();
        g = new Graph();
        while(input.hasNext()){
            set = input.nextLine();
            String edges[] = set.split(" ");
            String v1 = edges[0];
            String v2 = edges[1];
            g.addvertex(v1);
            g.addvertex(v2);
            checkedges.add(g.addEdge(v1, v2));
        }
        for(int result:checkedges){
            if (result == -1)
                brokenedge = false;
        }
        if (brokenedge == false){
            jTextField1.setText("Missing Vertex in Graph. Tree is broken.");
            //jTextField1.setText("Repeated Edge. Tree is Cyclic.");
        }
    }

