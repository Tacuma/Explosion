    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        jTextArea1.setText("");
        String transaction = jTextField4.getText();
        String dNode = jTextField9.getText();
        if (transaction == null || dNode == null)
            jTextArea1.setText("Enter a Value");
        ArrayList<ArrayList<String>> temp = g.allpath(jTextField4.getText(), jTextField9.getText());
        for (int i = 0; i < temp.size(); i++){
            for (String s: temp.get(i)){
                jTextArea1.append(s + " ");
            }
            jTextArea1.append("\n");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

